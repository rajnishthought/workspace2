package TreblePojo;

public class Geographies_Response_POJO {
	
	private int geography_id;
	private String geography_short_code;
	private String name;
	private String state_name;
	
	public int getGeography_id() {
		return geography_id;
	}
	public void setGeography_id(int geography_id) {
		this.geography_id = geography_id;
	}
	public String getGeography_short_code() {
		return geography_short_code;
	}
	public void setGeography_short_code(String geography_short_code) {
		this.geography_short_code = geography_short_code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getState_name() {
		return state_name;
	}
	public void setState_name(String state_name) {
		this.state_name = state_name;
	}
	
	

}
